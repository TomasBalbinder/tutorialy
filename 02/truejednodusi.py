# výpočet povrchu a obvodu čtverce 

strana = int(input("Zadej stranu čtverce pro výpočet objemu a povrchu: " ))



if strana >= 1:
    print("Povrch čtverce je", 6 * strana**2, "cm2")
elif strana >= 1:
    print("Povrch čtverce je", strana**3, "cm3")
else:
    print("Zkus to znovu!")

